using System;


    namespace programa
{
    class batatinha
    {
        static void Main(string[] args)
        {

            int x, impar;

            x = int.Parse(Console.ReadLine());


            for (int i = 0;  i <= x; i++)
            {
                impar = i % 2;
                if (impar == 1)
                {
                    Console.WriteLine(i);
                }
            }
        }
    }
}





